package Tenta1;

public class Fraga2 {
    private int alpha;
    private int beta;
    private int gamma;

    public Fraga2(int alpha, int beta, int gamma) {
        setAlpha(alpha);
        setBeta(beta);
        setGamma(gamma);
    }

    public void setAlpha(int alpha) {
        this.alpha = alpha;
    }

    public void setBeta(int beta) {
        this.beta = beta;
    }

    public void setGamma(int gamma) {
        this.gamma = gamma;
    }


}
